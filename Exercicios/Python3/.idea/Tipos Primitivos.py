# int()
n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
s = n1 + n2
print('A soma entre {} e {}, é igual {}.'.format(n1, n2, s))
# float()
n1 = float(input('Digite um número: '))
n2 = float(input('Digite outro número: '))
s = n1 + n2
print('A soma entre {} e {}, é igual {}.'.format(n1, n2, s))
